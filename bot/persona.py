# Якою мовою відповідає власник. Має збігатися з мовою STT/TTS (config.BOT_LANG).
_LANG_DIRECTIVE = {
    "ru": "Отвечай ТОЛЬКО на русском языке и ТОЛЬКО кириллицей. Категорически "
          "запрещено использовать иероглифы, китайские, английские или любые "
          "другие символы и слова.",
    "uk": "Відповідай ТІЛЬКИ українською мовою і ТІЛЬКИ кирилицею. Категорично "
          "заборонено використовувати ієрогліфи, китайські, англійські чи будь-які "
          "інші символи та слова.",
}


def build_system_prompt(scenario_id: int = 1, level: int = 1, lang: str = BOT_LANG) -> str:
    """Збирає системний промпт під сценарій, рівень і мову.

    lang береться з config.BOT_LANG, тому мова відповідей завжди збігається з STT/TTS.
    """
    scenario = SCENARIOS[scenario_id]
    lvl = LEVELS[level]
    directive = _LANG_DIRECTIVE.get(lang, _LANG_DIRECTIVE["ru"])
    return (
        f"{scenario['context']}\n\n"
        f"УРОВЕНЬ СЛОЖНОСТИ — {lvl['name']}: {lvl['behavior']}\n\n"
        f"{_BASE_RULES}\n- {directive}"
    )
